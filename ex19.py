# for loop
'''
a statement that will execute it's block of code a limited amount of time's
'''


for i in range(10):
    print(i+1,end=" ")

for i in range(50,100+1):
    print(i,end=" ")

for i in range(50,100+1,2):
    print(i,end=" ")

for i in "Hemanth Sai Nag":
    print(i,end=" ",sep=".")